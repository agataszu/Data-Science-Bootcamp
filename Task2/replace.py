phrase = "The!quick!brown!fox!jumps!over!the!lazy!dog." 

# Removing the "!" in the sentence by replacing it with an empty space.
phrase = phrase.replace("!", " ")
print(phrase)
print()

# Printing the corrected sentence in capital letters
phrase = phrase.upper()
print(phrase)
print()

# Printing the sentence in reverse
phrase = phrase.capitalize()
reverse_phrase = phrase[::-1]
print(reverse_phrase)
print()

